package com.thegruu.entidades;

public class Interesses {
	
	private int idInteresse;
	private String dsInteresse;
	private String dsImagenInteresse;
	private Boolean flAtivo;
	
	public int getIdInteresse() {
		return idInteresse;
	}
	public void setIdInteresse(int idInteresse) {
		this.idInteresse = idInteresse;
	}
	public String getDsInteresse() {
		return dsInteresse;
	}
	public void setDsInteresse(String dsInteresse) {
		this.dsInteresse = dsInteresse;
	}
	public String getDsImagenInteresse() {
		return dsImagenInteresse;
	}
	public void setDsImagenInteresse(String dsImagenInteresses) {
		this.dsImagenInteresse = dsImagenInteresses;
	}
	public Boolean getFlAtivos() {
		return flAtivo;
	}
	public void setFlAtivos(Boolean dsAtivos) {
		this.flAtivo = dsAtivos;
	}
	
}
