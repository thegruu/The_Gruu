package com.thegruu.entidades;

public class InteressesUsuarios {

	private String idUsuario;
	private String idInteresses;
	private boolean flAtivo;
	
	public String getIdUsuario() {
		return idUsuario;
	}
	public void setIdUsuario(String idUsuario) {
		this.idUsuario = idUsuario;
	}
	public String getIdInteresses() {
		return idInteresses;
	}
	public void setIdInteresses(String idInteresses) {
		this.idInteresses = idInteresses;
	}
	public boolean isFlAtivo() {
		return flAtivo;
	}
	public void setFlAtivo(boolean flAtivo) {
		this.flAtivo = flAtivo;
	}
	

	
	
}
